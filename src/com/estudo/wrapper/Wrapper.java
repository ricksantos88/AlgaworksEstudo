package com.estudo.wrapper;

public class Wrapper {
	
	public static void main(String[] args) {
		
		//Wrapper Class
		Integer i = new Integer(10);
		byte b = i.byteValue();
		System.out.println(b);
		System.out.println(b);
		
		// byte   - Byte
		// short  - Short
		// int    - Integer
		// long   - Long
		// double - Double
		// float  - Float
		// char   - Character
		
	}
	
}
